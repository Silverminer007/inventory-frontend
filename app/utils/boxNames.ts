// ~12 k unique combinations per shortCode (112 adj × 110 nouns).
// Collision detection is intentionally omitted — the space is large enough
// that the rare duplicate is acceptable, and names are user-editable anyway.
export function generateBoxName(shortCode: string): string {
  const adj = ADJECTIVES[Math.floor(Math.random() * ADJECTIVES.length)]!
  const noun = NOUNS[Math.floor(Math.random() * NOUNS.length)]!
  return `${shortCode}-${adj}-${noun}`
}
